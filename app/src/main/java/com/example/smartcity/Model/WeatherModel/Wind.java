package com.example.smartcity.Model.WeatherModel;

public class Wind {

    private double speed ;
    private double deg ;

    public Wind(){}

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        speed = speed;
    }

    public double getDeg() {
        return deg;
    }

    public void setDeg(double deg) {
        deg = deg;
    }
}
